package com.sil.calculator;

public class Calculator {
    public static int add(int a, int b) {
        return (a + b);
    }

    public static int sub(int a, int b) {
        return (a - b);
    }

    public static int mult(int a, int b) {
        return (a * b);
    }

    public static int dev(int a, int b) {
        return (a / b);
    }
}
